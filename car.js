class car
{
    constructor(opts)
    {
        this.make = opts.make;
        this.model = opts.model;
        this.color = opts.color;
        this.yearMade = opts.yearMade;
        this.mpg = opts.mpg;
        this.typeOfCar = opts.typeOfCar;
        this.engine = opts.engine;
        this.horsepower = opts.horsepower;
    }
    getMake()
    {
        return this.make;
    }
    getModel()
    {
        return this.make;
    }

    
}